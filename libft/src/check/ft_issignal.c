#include "libft.h"

/* -------------------------------------------------------------------------- **
** FUNCTION: ft_issignal
** -------------------------------------------------------------------------- **
** DESCRIPTION:
** Check if the characters is signal '+' or '-'.
** PARAMETERS:
** #c. The character
** RETURN VALUES:
** Return 1 if char is signal and 0 if its another char.
** -------------------------------------------------------------------------- */
int	ft_issignal(int c)
{
	return (c == '+' || c == '-');
}
